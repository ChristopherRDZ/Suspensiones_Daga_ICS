    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String aux_1 = jTextField1.getText();
        String aux_2 = jTextField2.getText();
        String aux_3 = jTextField3.getText();
        String aux_4 = jTextField4.getText();
        String aux_5 = jTextField5.getText();
        if (!"".equals(aux_1) && !"".equals(aux_2) && !"".equals(aux_3) && !"".equals(aux_4) && !"".equals(aux_5)){
            String sql = "INSERT INTO products (id, marca, descripcion, cantidad, precio) VALUES (?,?,?,?,?)";
            try{
                PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, aux_1);
                pst.setString(2, aux_2);
                pst.setString(3, aux_5);
                pst.setString(4, aux_3);
                pst.setString(5, aux_4);
                int n = pst.executeUpdate();
                if (n > 0){
                    inventario.cargar_tabla();
                    JOptionPane.showMessageDialog(null, "Producto agregado con exito.");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField1.requestFocus();
                    
                }
            }catch (SQLException ex){
                Logger.getLogger(NuevoUser.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Ingrese valores correctos.");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField3.requestFocus();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Falta llenar algun campo.");
            jTextField1.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

