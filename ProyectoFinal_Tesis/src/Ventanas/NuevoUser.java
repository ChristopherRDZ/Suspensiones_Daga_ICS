    //Boton para crear el usuario
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jTextField1.getText();
        String pass = jTextField2.getText();
        if (user.equals("") && pass.equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese un usuario y una contrase単a.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }else{
            if (user.equals("")){
                JOptionPane.showMessageDialog(this, "Ingrese un usuario.", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
            }
            if (pass.equals("")){
                JOptionPane.showMessageDialog(this, "Ingrese una contrase単a.", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField2.requestFocus();
            }
            if (!"".equals(user) && !"".equals(pass)){
                String sql = "INSERT INTO users (usuario, contrase単a) VALUES (?,?)";
                try{
                    PreparedStatement pst = cn.prepareStatement(sql);
                    pst.setString(1, user);
                    pst.setString(2, pass);
                    int n = pst.executeUpdate();
                    if (n > 0){
                        JOptionPane.showMessageDialog(this, "Usuario registrado con exito.");
                        this.setVisible(false);
                    }
                }catch (SQLException ex){
                    Logger.getLogger(NuevoUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

